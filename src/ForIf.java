public class ForIf {
    public static void main(String[] args) {
        for (int i = 0; i <= 20; i++ ){
            System.out.println(i);
            if(i == 10){
                System.out.println( "Half way");
            };
            if(i==20){
                System.out.println("End");
            }
        }
    }
}
