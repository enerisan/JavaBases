public class DoWhilePoney {
    public static void main(String[] args) {
        int poney = 0;

        do {
            System.out.println(poney +" Poney ");
            poney ++;

        } while  (poney <=1000000);


        do {
            System.out.println(poney +" Poney ");
            poney --;

        } while  (poney <=1000000 && poney >=0);
    }
}
